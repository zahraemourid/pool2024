#include <unistd.h>

int	main(int ac, char **argv)
{
	int	i;
	int	j;

	i = 0;
	j = ac - 1;
	if (ac > 1)
	{
		while (j > 0)
		{
			while (argv[j][i] != '\0')
			{
				write(1, &argv[j][i], 1);
				i++;
			}
			write(1, "\n", 1);
			j--;
			i = 0;
		}
	}
	return (0);
}
